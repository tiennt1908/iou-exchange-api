interface ERC20Params {
  rpc: string,
  token: string,
}
export const ERC20API = ({ rpc, token }: ERC20Params) => {
  const web3 = new Web3(rpc);
  const contract = new web3.eth.Contract(ABI, token);
  return {
    approve(spender: string, value: number) {
      return contract.methods.approve(spender, "0x" + Math.floor(value).toString(16)).encodeABI();
    },
    getName(): Promise<string> {
      return contract.methods.name().call();
    },
    getBalanceOf(user: string): Promise<string> {
      return contract.methods.balanceOf(user).call();
    },
    getSymbol(): Promise<string> {
      return contract.methods.symbol().call();
    },
    getDecimals(): Promise<string> {
      return contract.methods.decimals().call();
    },
    getTotalSupply(): Promise<string> {
      return contract.methods.totalSupply().call();
    },
    getAllowance(owner: string, spender: string): Promise<string> {
      return contract.methods.allowance(owner, spender).call();
    }
  }
}
